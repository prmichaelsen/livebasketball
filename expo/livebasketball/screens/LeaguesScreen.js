import React from 'react';
import {
  Image,
  Platform,
  ScrollView,
  StyleSheet,
  Text,
  TouchableOpacity,
  View,
  FlatList,
  Button,
} from 'react-native';
import { WebBrowser } from 'expo';
import { MonoText } from '../components/StyledText';

import { db } from '../navigation/RootNavigation';

import
{
  SettingsDividerShort,
  SettingsDividerLong,
  SettingsEditText,
  SettingsCategoryHeader,
  SettingsSwitch,
  SettingsPicker
} from 'react-native-settings-components';

export default class LeaguesScreen extends React.Component {
  static navigationOptions = {
    title: 'Leagues',
  };

  componentWillMount() {
    db.ref('leagues').on('value', (snapshot) => {
      var leagues = snapshot.val();
      this.setState({ 
        leagues: leagues ? Object.keys(leagues).map(id => { leagues[id].uuid = id; return leagues[id] }) : [],
        leaguesData: leagues,
      });
    });
  }

  state = {
    leagues: [],
    leaguesData: {},
  }; 

  render() {
    return (
      <View style={styles.container}>
        <ScrollView style={styles.container} contentContainerStyle={styles.contentContainer}> 
          <View>
            <FlatList
              data={this.state.leagues}
              renderItem={({ item }) => {
                return (
                  <SettingsSwitch
                    title={item.country + item.name}
                    onSaveValue={(value) => {
                      db.ref('leagues/' + item.uuid).set({
                        ...(this.state.leaguesData[item.uuid]),
                        enabled: value,
                      })
                    }}
                    value={item.enabled}
                    thumbTintColor={(item.enabled) ? colors.switchEnabled : colors.switchDisabled}
                    disabled={false}
                  />
                );
              }}
              keyExtractor={(item) => item.uuid} 
            /> 
          </View>
        </ScrollView> 
      </View>
    );
  }

  _maybeRenderDevelopmentModeWarning() {
    if (__DEV__) {
      const learnMoreButton = (
        <Text onPress={this._handleLearnMorePress} style={styles.helpLinkText}>
          Learn more
        </Text>
      );

      return (
        <Text style={styles.developmentModeText}>
          Development mode is enabled, your app will be slower but you can use useful development
          tools. {learnMoreButton}
        </Text>
      );
    } else {
      return (
        <Text style={styles.developmentModeText}>
          You are not in development mode, your app will run at full speed.
        </Text>
      );
    }
  }

  _handleLearnMorePress = () => {
    WebBrowser.openBrowserAsync('https://docs.expo.io/versions/latest/guides/development-mode');
  };

  _handleHelpPress = () => {
    WebBrowser.openBrowserAsync(
      'https://docs.expo.io/versions/latest/guides/up-and-running.html#can-t-see-your-changes'
    );
  };
}

